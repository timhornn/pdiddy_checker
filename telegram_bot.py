def split_into_logical_parts(text, max_length):
    """
    –†–∞–∑–±–∏–≤–∞–µ—Ç —Ç–µ–∫—Å—Ç –Ω–∞ –ª–æ–≥–∏—á–µ—Å–∫–∏–µ —á–∞—Å—Ç–∏, —Å–æ—Ö—Ä–∞–Ω—è—è —Å—Ç—Ä—É–∫—Ç—É—Ä—É –æ—Ç—á–µ—Ç–∞
    """
    # –°–Ω–∞—á–∞–ª–∞ –ø–æ–ø—Ä–æ–±—É–µ–º —Ä–∞–∑–±–∏—Ç—å –ø–æ –∑–∞–≥–æ–ª–æ–≤–∫–∞–º
    sections = re.split(r'(\n[–ê-–Ø][^:]+:\s*\n)', text)

    parts = []
    current_part = ""

    # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Ä–∞–∑–¥–µ–ª—ã
    for i in range(len(sections)):
        # –ï—Å–ª–∏ —ç—Ç–æ –∑–∞–≥–æ–ª–æ–≤–æ–∫, –æ–±—ä–µ–¥–∏–Ω—è–µ–º —Å —Å–æ–¥–µ—Ä–∂–∏–º—ã–º
        if i > 0 and re.match(r'\n[–ê-–Ø][^:]+:\s*\n', sections[i]):
            section = sections[i] + sections[i + 1] if i + 1 < len(sections) else sections[i]
            i += 1
        else:
            section = sections[i]

        # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∞—è —á–∞—Å—Ç—å + –Ω–æ–≤—ã–π —Ä–∞–∑–¥–µ–ª –Ω–µ –ø—Ä–µ–≤—ã—à–∞–µ—Ç –ª–∏–º–∏—Ç
        if len(current_part) + len(section) <= max_length:
            current_part += section
        else:
            # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∞—è —á–∞—Å—Ç—å –Ω–µ –ø—É—Å—Ç–∞—è, —Å–æ—Ö—Ä–∞–Ω—è–µ–º –µ–µ
            if current_part.strip():
                parts.append(current_part.strip())
                current_part = ""

            # –ï—Å–ª–∏ —Ä–∞–∑–¥–µ–ª —Å–∞–º –ø–æ —Å–µ–±–µ –º–µ–Ω—å—à–µ –ª–∏–º–∏—Ç–∞
            if len(section) <= max_length:
                current_part = section
            else:
                # –î–µ–ª–∏–º –¥–ª–∏–Ω–Ω—ã–π —Ä–∞–∑–¥–µ–ª –Ω–∞ –∞–±–∑–∞—Ü—ã
                paragraphs = section.split('\n\n')
                temp_section = ""

                for p in paragraphs:
                    if len(temp_section) + len(p) + 2 <= max_length:
                        temp_section += p + "\n\n"
                    else:
                        if temp_section.strip():
                            parts.append(temp_section.strip())
                        temp_section = p + "\n\n"

                if temp_section.strip():
                    current_part = temp_section

    # –î–æ–±–∞–≤–ª—è–µ–º –æ—Å—Ç–∞–≤—à—É—é—Å—è —á–∞—Å—Ç—å
    if current_part.strip():
        parts.append(current_part.strip())

    # –ï—Å–ª–∏ –≤—Å–µ –∂–µ –ø–æ–ª—É—á–∏–ª—Å—è —Å–ª–∏—à–∫–æ–º –¥–ª–∏–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç, –¥–µ–ª–∏–º –Ω–∞ —Ä–∞–≤–Ω—ã–µ —á–∞—Å—Ç–∏
    if not parts:
        parts = [text[i:i + max_length] for i in range(0, len(text), max_length)]

    return parts


